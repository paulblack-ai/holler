"""Holler CLI -- four commands to first call.

pip install holler -> holler init -> holler trunk add -> holler call
"""
import asyncio
import os
import subprocess
import sys
import time
from pathlib import Path

import click
import structlog

logger = structlog.get_logger()


def _get_project_root() -> Path:
    """Locate the project root (parent of holler/ package).

    Navigates: commands.py -> cli/ -> holler/ -> project_root/
    Works regardless of the user's current working directory.
    """
    return Path(__file__).resolve().parent.parent.parent


@click.group()
@click.version_option(package_name="holler")
def cli():
    """Holler -- voice and text infrastructure for AI agents.

    You don't need permission to holler.
    """
    pass


@cli.command()
def init():
    """Download models and start local services.

    Downloads faster-whisper and Kokoro ONNX models, generates .holler.env
    config with defaults, and starts FreeSWITCH + Redis via Docker Compose.
    """
    click.echo("Holler init -- setting up local infrastructure")
    click.echo()

    # 1. Check GPU availability
    _check_gpu()

    # 2. Download models
    _download_models()

    # 3. Generate .holler.env if it doesn't exist
    _generate_env_file()

    # 4. Start Docker Compose services
    _start_services()

    click.echo()
    click.secho("Holler initialized.", fg="green", bold=True)
    click.echo("Next: holler trunk add")


@cli.command("trunk")
@click.option("--host", prompt="SIP trunk host", help="SIP trunk hostname or IP")
@click.option("--user", prompt="Username", help="SIP authentication username")
@click.option(
    "--password",
    "--pass",
    prompt="Password",
    hide_input=True,
    help="SIP authentication password (alias: --pass, per D-15)",
)
def trunk_add(host, user, password):
    """Configure SIP trunk credentials.

    Writes trunk config to .holler.env. Use --host, --user, --pass
    flags for non-interactive mode.
    """
    _write_trunk_config(host, user, password)
    click.secho(f"Trunk configured: {user}@{host}", fg="green")
    click.echo("Next: holler call +1XXXXXXXXXX")


@cli.command("call")
@click.argument("destination")
@click.option("--agent", default=None, help="System prompt for the call agent")
def call(destination, agent):
    """Place a call to DESTINATION (E.164 format, e.g. +14155551234).

    Uses a default voice assistant prompt. Override with --agent "your prompt".
    """
    structlog.configure(
        processors=[
            structlog.processors.TimeStamper(fmt="iso"),
            structlog.processors.add_log_level,
            structlog.dev.ConsoleRenderer(),
        ],
    )
    click.echo(f"Calling {destination}...")
    from holler.main import main
    asyncio.run(main(call_destination=destination, agent_prompt=agent))


# --- Helper functions ---

def _check_gpu():
    """Check for GPU availability and report."""
    try:
        import torch
        if torch.cuda.is_available():
            gpu_name = torch.cuda.get_device_name(0)
            click.echo(f"  GPU detected: {gpu_name}")
        else:
            click.echo("  No GPU detected -- using CPU (slower, limited concurrent calls)")
    except ImportError:
        click.echo("  No GPU detected -- using CPU (slower, limited concurrent calls)")


def _download_models():
    """Download faster-whisper and Kokoro ONNX models."""
    click.echo("  Downloading models...")

    # faster-whisper model (uses huggingface_hub internally)
    try:
        click.echo("    faster-whisper distil-large-v3...", nl=False)
        from faster_whisper import WhisperModel
        WhisperModel("distil-large-v3", device="cpu", compute_type="int8")
        click.secho(" done", fg="green")
    except Exception as e:
        click.secho(f" failed: {e}", fg="red")

    # Kokoro ONNX model
    try:
        click.echo("    Kokoro ONNX model...", nl=False)
        from huggingface_hub import hf_hub_download
        hf_hub_download("fastrtc/kokoro-onnx", "kokoro-v1.0.onnx")
        hf_hub_download("fastrtc/kokoro-onnx", "voices-v1.0.bin")
        click.secho(" done", fg="green")
    except Exception as e:
        click.secho(f" failed: {e}", fg="red")


