def _write_trunk_config(host, user, password):
    """Write SIP trunk credentials to .holler.env."""
    env_path = ".holler.env"
    lines = []
    if os.path.exists(env_path):
        with open(env_path) as f:
            lines = f.readlines()

    # Update or append trunk config
    trunk_vars = {
        "HOLLER_TRUNK_HOST": host,
        "HOLLER_TRUNK_USER": user,
        "HOLLER_TRUNK_PASS": password,
    }
    updated_keys = set()
    new_lines = []
    for line in lines:
        key = line.split("=", 1)[0].strip()
        if key in trunk_vars:
            new_lines.append(f"{key}={trunk_vars[key]}\n")
            updated_keys.add(key)
        else:
            new_lines.append(line)

    # Append any keys not found in existing file
    for key, value in trunk_vars.items():
        if key not in updated_keys:
            new_lines.append(f"{key}={value}\n")

    with open(env_path, "w") as f:
        f.writelines(new_lines)


def _start_services():
    """Start Docker Compose services and wait for health."""
    click.echo("  Starting Docker Compose services...")

    # Check HOLLER_COMPOSE_FILE env var as an explicit override first
    env_compose = os.environ.get("HOLLER_COMPOSE_FILE")
    if env_compose:
        compose_file = Path(env_compose)
        if not compose_file.exists():
            click.secho(f"  HOLLER_COMPOSE_FILE={env_compose} not found", fg="red")
            return
    else:
        # Primary: resolve from package location (works in source checkout / editable install)
        project_root = _get_project_root()
        compose_file = project_root / "docker" / "docker-compose.yml"

        # Fallback: check CWD (works when user is in project dir with pip install)
        if not compose_file.exists():
            compose_file = Path.cwd() / "docker" / "docker-compose.yml"

        if not compose_file.exists():
            click.secho(
                "  docker-compose.yml not found. Run from the holler project directory,\n"
                "  or set HOLLER_COMPOSE_FILE to the path of your docker-compose.yml.",
                fg="red",
            )
            return

    try:
        result = subprocess.run(
            ["docker", "compose", "-f", str(compose_file),
             "--project-directory", str(compose_file.parent),
             "up", "-d"],
            capture_output=True, text=True, timeout=120
        )
        if result.returncode != 0:
            click.secho(f"  Docker Compose failed: {result.stderr}", fg="red")
            return

        # Wait for FreeSWITCH ESL port to be ready
        click.echo("  Waiting for FreeSWITCH...", nl=False)
        import socket
        for _ in range(30):  # 30 second timeout
            try:
                sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
                sock.settimeout(1)
                sock.connect(("127.0.0.1", 8021))
                sock.close()
                click.secho(" ready", fg="green")
                return
            except (ConnectionRefusedError, socket.timeout, OSError):
                time.sleep(1)
        click.secho(" timeout (FreeSWITCH not responding on port 8021)", fg="yellow")
    except FileNotFoundError:
        click.secho("  Docker not found -- install Docker and try again", fg="red")
    except subprocess.TimeoutExpired:
        click.secho("  Docker Compose timed out", fg="red")
